// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  var board = '';
  //need two for loops so we can work in a 2D space
  for (var i = 0; i < num; i++) { 
    for (var j = 0; j < num; j++) { 
      if ((i + j) % 2 === 0) { 
        //j returned uniform columns
        board += ' ';
      } else {
        board += '#';
      }

    }
    
    board += '\n';

  }
  
  console.log(board);

}

